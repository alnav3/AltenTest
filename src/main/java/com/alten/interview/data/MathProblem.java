package com.alten.interview.data;


public class MathProblem {
	private Integer x;
	private Integer y;
	private Integer n;
	public MathProblem(Integer x, Integer y, Integer n) {
		super();
		this.x = x;
		this.y = y;
		this.n = n;
	}
	public MathProblem() {
		super();
	}
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	public Integer getN() {
		return n;
	}
	public void setN(Integer n) {
		this.n = n;
	}

}
